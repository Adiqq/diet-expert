package sample;

public class Preferuj {
    private String tag;
    private String tag2;

    public Preferuj(String tag, String tag2) {
        this.tag = tag;
        this.tag2 = tag2;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public String getTag2() {
        return tag2;
    }

    public void setTag2(String tag2) {
        this.tag2 = tag2;
    }
}
